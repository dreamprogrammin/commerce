-- =====================================================================================
-- МИГРАЦИЯ: Исправление SECURITY DEFINER для view bonus_system_status
-- =====================================================================================
-- Проблема: View создан с SECURITY DEFINER (по умолчанию), что позволяет
-- обходить RLS и выполняться с правами создателя view
--
-- Решение: Переключаем на SECURITY INVOKER - view будет выполняться
-- с правами текущего пользователя
-- =====================================================================================

-- Переключаем view на SECURITY INVOKER
ALTER VIEW public.bonus_system_status SET (security_invoker = true);

-- Проверка
DO $$
BEGIN
  RAISE NOTICE 'View bonus_system_status переключен на SECURITY INVOKER';
END $$;
